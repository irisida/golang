package main

import "fmt"

func longFormVariables() {
	/*
		longform method using the var keyword, the type
		and the initalisation value.
	*/
	var xx int = 7

	/*
		non initialised method that declares the variable
		with its type and identifier name.
	*/
	var s1 string
	s1 = "Go programming"

	fmt.Printf("xx = %v \n", x)
	fmt.Printf("s1 = %v \n", s1)
}
